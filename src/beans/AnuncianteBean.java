package beans;

public class AnuncianteBean {

	private int idAnunciante;
	private String nomeAnunciante, nomeServicoAnunciante, senhaAnunciante, emailAnunciante, telefoneAnunciante, cpfAnunciante, enderecoAnunciante;
	
	public int getIdAnunciante() {
		return idAnunciante;
	}
	public void setIdAnunciante(int idAnunciante) {
		this.idAnunciante = idAnunciante;
	}
	public String getNomeAnunciante() {
		return nomeAnunciante;
	}
	public void setNomeAnunciante(String nomeAnunciante) {
		this.nomeAnunciante = nomeAnunciante;
	}
	public String getNomeServicoAnunciante() {
		return nomeServicoAnunciante;
	}
	public void setNomeServicoAnunciante(String nomeServicoAnunciante) {
		this.nomeServicoAnunciante = nomeServicoAnunciante;
	}
	public String getSenhaAnunciante() {
		return senhaAnunciante;
	}
	public void setSenhaAnunciante(String senhaAnunciante) {
		this.senhaAnunciante = senhaAnunciante;
	}
	public String getEmailAnunciante() {
		return emailAnunciante;
	}
	public void setEmailAnunciante(String emailAnunciante) {
		this.emailAnunciante = emailAnunciante;
	}
	public String getTelefoneAnunciante() {
		return telefoneAnunciante;
	}
	public void setTelefoneAnunciante(String telefoneAnunciante) {
		this.telefoneAnunciante = telefoneAnunciante;
	}
	public String getCpfAnunciante() {
		return cpfAnunciante;
	}
	public void setCpfAnunciante(String cpfAnunciante) {
		this.cpfAnunciante = cpfAnunciante;
	}
	public String getEnderecoAnunciante() {
		return enderecoAnunciante;
	}
	public void setEnderecoAnunciante(String enderecoAnunciante) {
		this.enderecoAnunciante = enderecoAnunciante;
	}
	
}
